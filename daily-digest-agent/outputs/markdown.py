from datetime import datetime

from agent.models import DigestItem


def render_markdown(
    items: list[DigestItem],
    intro: str,
    min_score: float = 6.0,
    profile_name: str = "Daily Digest",
) -> str:
    date_str = datetime.now().strftime("%A, %B %d %Y")
    lines = [
        f"# {profile_name}",
        f"*{date_str}*",
        "",
        f"> {intro}",
        "",
        "---",
        "",
    ]

    filtered = [i for i in items if i.relevance_score >= min_score]

    if not filtered:
        lines.append("*No items above relevance threshold today.*")
        return "\n".join(lines)

    # Group by source category
    by_source: dict[str, list[DigestItem]] = {}
    for item in filtered:
        by_source.setdefault(item.source, []).append(item)

    for source, source_items in by_source.items():
        lines.append(f"## {source}")
        lines.append("")
        for item in source_items:
            score_bar = _score_bar(item.relevance_score)
            tags = "  ".join(f"`{t}`" for t in item.tags) if item.tags else ""
            lines.append(f"### [{item.title}]({item.url})")
            lines.append(f"{score_bar} {tags}")
            lines.append("")
            if item.ai_summary:
                lines.append(f"{item.ai_summary}")
                lines.append("")
        lines.append("")

    lines += [
        "---",
        f"*Generated by daily-digest-agent · {len(filtered)} items · "
        f"{datetime.now().strftime('%H:%M')}*",
    ]
    return "\n".join(lines)


def _score_bar(score: float) -> str:
    filled = round(score)
    empty = 10 - filled
    return f"{'█' * filled}{'░' * empty} {score:.1f}/10"
